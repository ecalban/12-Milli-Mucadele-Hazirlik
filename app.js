function normalizeAnswer(value) {
  return value.trim().replace(/[.]+$/, "").toLocaleLowerCase("tr-TR");
}

function getAnswerKey(value) {
  return normalizeAnswer(value).replace(/\s*\([^)]*\)\s*/g, " ").trim();
}

function getAnswerTokens(value) {
  return getAnswerKey(value)
    .replace(/[“”"().,;:!?[\]]/g, " ")
    .replace(/[’']/g, " ")
    .split(/\s+/)
    .filter(Boolean);
}

function getStemmedAnswerTokens(value) {
  return getAnswerTokens(value).map((token) => {
    if (token.length < 6) {
      return token;
    }

    return token
      .replace(/(nın|nin|nun|nün)$/u, "")
      .replace(/(ın|in|un|ün)$/u, "")
      .replace(/(na|ne|da|de|dan|den)$/u, "");
  });
}

function containsTokenSequence(tokens, sequence) {
  if (sequence.length === 0 || sequence.length > tokens.length) {
    return false;
  }

  return tokens.some((_, index) => (
    index + sequence.length <= tokens.length
    && sequence.every((token, offset) => token === tokens[index + offset])
  ));
}

function areAnswersSimilar(firstAnswer, secondAnswer) {
  const firstKey = getAnswerKey(firstAnswer);
  const secondKey = getAnswerKey(secondAnswer);

  if (firstKey === secondKey) {
    return true;
  }

  const tokenSets = [
    [getAnswerTokens(firstAnswer), getAnswerTokens(secondAnswer)],
    [getStemmedAnswerTokens(firstAnswer), getStemmedAnswerTokens(secondAnswer)],
  ];

  return tokenSets.some(([firstTokens, secondTokens]) => {
    const shorter = firstTokens.length <= secondTokens.length ? firstTokens : secondTokens;
    const longer = firstTokens.length > secondTokens.length ? firstTokens : secondTokens;

    return shorter.length <= 4 && containsTokenSequence(longer, shorter);
  });
}

