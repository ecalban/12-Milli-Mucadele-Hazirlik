function toggleHintPanel() {
  const willOpen = els.hintPanel.hidden;
  els.hintPanel.hidden = !willOpen;
  els.hintToggleButton.classList.toggle("is-active", willOpen);
  els.hintToggleButton.setAttribute("aria-expanded", String(willOpen));

  if (willOpen) {
    renderSelectedHint();
    els.hintPanel.scrollIntoView({ behavior: "smooth", block: "nearest" });
  }
}

function updateUi() {
  const placedCount = state.placed.size;
  const totalCount = FACTS.length;
  const allPlaced = placedCount === totalCount && state.currentId === null;

  els.placedCount.textContent = String(placedCount);
  els.totalCount.textContent = String(totalCount);
  els.drawButton.disabled = state.currentId !== null || state.remaining.length === 0;
  els.finishButton.disabled = !allPlaced || state.checked;

  const hasAssignableCard = state.selectedId !== null || state.currentId !== null;
  els.hintToggleButton.disabled = !hasAssignableCard;
  els.choiceBar.querySelectorAll(".choice-button").forEach((button) => {
    button.disabled = !hasAssignableCard;
  });

  if (state.checked) {
    els.statusText.textContent = `Kontrol edildi: ${state.lastScore} / ${totalCount} doğru.`;
  } else if (allPlaced) {
    els.statusText.textContent = "Tüm bilgiler yerleştirildi.";
  } else if (state.selectedId !== null) {
    els.statusText.textContent = "Kart seçili.";
  } else if (state.currentId !== null) {
    els.statusText.textContent = "Kart hazır.";
  } else {
    els.statusText.textContent = `${state.remaining.length} bilgi kaldı.`;
  }

  updateColumnCounts();
  if (!els.hintPanel.hidden) {
    renderSelectedHint();
  }
}

function removeResultNote(card) {
  card.querySelector(".result-note")?.remove();
}

function finishRound() {
  if (state.placed.size !== FACTS.length || state.currentId !== null) {
    updateUi();
    return;
  }

  let correctCount = 0;
  state.selectedId = null;

  document.querySelectorAll(".drop-zone .knowledge-card").forEach((card) => {
    const fact = factById.get(Number(card.dataset.id));
    const chosenCategory = card.closest(".drop-zone").dataset.category;
    const isCorrect = chosenCategory === fact.category;

    card.classList.remove("is-selected", "correct", "wrong");
    card.setAttribute("aria-pressed", "false");
    card.classList.add(isCorrect ? "correct" : "wrong");
    removeResultNote(card);

    const note = document.createElement("div");
    note.className = "result-note";
    note.textContent = isCorrect ? "Doğru" : "Yeniden düşün";
    card.append(note);

    if (isCorrect) {
      correctCount += 1;
    }
  });

  state.checked = true;
  state.lastScore = correctCount;
  renderResults(correctCount);
  updateUi();
}

function renderResults(correctCount) {
  const totalCount = FACTS.length;
  const wrongCount = totalCount - correctCount;
  const scoreWidth = `${Math.round((correctCount / totalCount) * 100)}%`;

  els.resultsPanel.hidden = false;
  els.resultsPanel.innerHTML = `
    <h2>Kontrol: ${correctCount} / ${totalCount}</h2>
    <div class="result-meter">
      <div class="meter-track"><span class="meter-fill" style="--score-width: ${scoreWidth}"></span></div>
      <div class="result-line">
        <span>${correctCount} doğru</span>
        <span>${wrongCount} yeniden düşün</span>
      </div>
    </div>
  `;
  els.resultsPanel.scrollIntoView({ behavior: "smooth", block: "nearest" });
}

function resetRound() {
  state.remaining = shuffle(FACTS.map((fact) => fact.id));
  state.currentId = null;
  state.selectedId = null;
  state.placed.clear();
  state.checked = false;
  state.lastScore = null;
  els.resultsPanel.hidden = true;
  els.resultsPanel.innerHTML = "";
  renderColumns();
  renderChoices();
  setCurrentEmpty();
  updateUi();
}

function countAssignedAnswers() {
  return [...matchState.assigned.values()].reduce((total, answerIds) => total + answerIds.length, 0);
}

function countCorrectAnswers(definition, assignedAnswerIds) {
  const expectedCounts = new Map();
  definition.answers.forEach((answer) => {
    expectedCounts.set(answer, (expectedCounts.get(answer) ?? 0) + 1);
  });

  let correctCount = 0;
  assignedAnswerIds.forEach((answerId) => {
    const answer = matchAnswerById.get(answerId);
    const remaining = answer ? expectedCounts.get(answer.text) ?? 0 : 0;
    if (remaining > 0) {
      correctCount += 1;
      expectedCounts.set(answer.text, remaining - 1);
    }
  });

  return correctCount;
}

function createDefinitionCard(definition) {
  const assignedAnswerIds = matchState.assigned.get(definition.id) ?? [];
  const assignedAnswers = assignedAnswerIds.map((answerId) => matchAnswerById.get(answerId)).filter(Boolean);
  const expectedCount = definition.answers.length;
  const card = document.createElement("div");
  card.className = "definition-card";
  card.dataset.definitionId = String(definition.id);
  card.tabIndex = 0;
  card.setAttribute("role", "button");
  card.setAttribute("aria-label", definition.definition);
  card.setAttribute("aria-pressed", String(matchState.selectedDefinitionId === definition.id));

  if (matchState.selectedDefinitionId === definition.id) {
    card.classList.add("is-selected");
  }

  if (assignedAnswers.length > 0) {
    card.classList.add("is-assigned");
  }

  if (matchState.checked) {
    const isCorrect = countCorrectAnswers(definition, assignedAnswerIds) === expectedCount;
    card.classList.add(isCorrect ? "correct" : "wrong");
  }

  const text = document.createElement("div");
  text.className = "definition-text";
  text.textContent = definition.definition;
  card.append(text);

  if (assignedAnswers.length > 0) {
    const assignedList = document.createElement("div");
    assignedList.className = "assigned-answer-list";
    const label = document.createElement("span");
    label.className = "assigned-answer-label";
    label.textContent = "Seçilen cevaplar";
    assignedList.append(label);

    assignedAnswers.forEach((answer) => {
      const answerButton = document.createElement("button");
      answerButton.className = "assigned-answer-chip";
      answerButton.type = "button";
      answerButton.setAttribute("aria-label", `${answer.text} cevabını kaldır`);
      answerButton.textContent = answer.text;

      const removeMark = document.createElement("span");
      removeMark.setAttribute("aria-hidden", "true");
      removeMark.textContent = "×";
      answerButton.append(removeMark);

      answerButton.addEventListener("click", (event) => removeAssignedAnswer(definition.id, answer.id, event));
      assignedList.append(answerButton);
    });

    card.append(assignedList);
  }

  if (matchState.checked) {
    const note = document.createElement("div");
    note.className = "match-result-note";
    note.textContent = countCorrectAnswers(definition, assignedAnswerIds) === expectedCount ? "Doğru" : "Yeniden düşün";
    card.append(note);
  }

  card.addEventListener("click", () => selectDefinition(definition.id));
  card.addEventListener("keydown", (event) => {
    if (event.key === "Enter" || event.key === " ") {
      event.preventDefault();
      selectDefinition(definition.id);
    }
  });
  return card;
}

function createAnswerCard(answer) {
  const button = document.createElement("button");
  button.className = "answer-card";
  button.type = "button";
  button.dataset.answerId = String(answer.id);
  const text = document.createElement("span");
  text.className = "answer-text";
  text.textContent = answer.text;
  button.append(text);
  button.addEventListener("click", () => assignAnswerToSelected(answer.id));
  return button;
}

function renderMatchingBoard() {
  const usedAnswerIds = new Set([...matchState.assigned.values()].flat());
  els.definitionList.innerHTML = "";
  els.answerList.innerHTML = "";

  matchState.definitionOrder.forEach((definitionId) => {
    els.definitionList.append(createDefinitionCard(matchDefinitionById.get(definitionId)));
  });

  matchState.answerOrder.forEach((answerId) => {
    if (!usedAnswerIds.has(answerId)) {
      els.answerList.append(createAnswerCard(matchAnswerById.get(answerId)));
    }
  });

  updateMatchingUi();
}

function selectDefinition(definitionId) {
  matchState.selectedDefinitionId = definitionId;
  renderMatchingBoard();
}

function clearMatchingCheckState() {
  if (!matchState.checked) {
    return;
  }

  matchState.checked = false;
  matchState.lastScore = null;
  els.matchResultsPanel.hidden = true;
  els.matchResultsPanel.innerHTML = "";
}

function assignAnswerToSelected(answerId) {
  if (matchState.selectedDefinitionId === null) {
    els.matchStatusText.textContent = "Tanım bekliyor.";
    return;
  }

  const definition = matchDefinitionById.get(matchState.selectedDefinitionId);
  const assignedAnswerIds = matchState.assigned.get(matchState.selectedDefinitionId) ?? [];
  if (assignedAnswerIds.length >= definition.answers.length) {
    els.matchStatusText.textContent = "Bu tanım için yeterli cevap seçildi.";
    return;
  }

  clearMatchingCheckState();
  const nextAssignedAnswerIds = [...assignedAnswerIds, answerId];
  matchState.assigned.set(matchState.selectedDefinitionId, nextAssignedAnswerIds);
  if (nextAssignedAnswerIds.length >= definition.answers.length) {
    matchState.selectedDefinitionId = null;
  }
  renderMatchingBoard();
}

function removeAssignedAnswer(definitionId, answerId, event) {
  event.stopPropagation();
  clearMatchingCheckState();
  const assignedAnswerIds = matchState.assigned.get(definitionId) ?? [];
  const nextAssignedAnswerIds = assignedAnswerIds.filter((assignedAnswerId) => assignedAnswerId !== answerId);
  if (nextAssignedAnswerIds.length > 0) {
    matchState.assigned.set(definitionId, nextAssignedAnswerIds);
  } else {
    matchState.assigned.delete(definitionId);
  }
  matchState.selectedDefinitionId = definitionId;
  renderMatchingBoard();
}

function updateMatchingUi() {
  const assignedCount = countAssignedAnswers();
  const totalCount = MATCH_PAIRS.length;
  const remainingAnswerCount = totalCount - assignedCount;
  const allAssigned = assignedCount === totalCount;

  els.matchPlacedCount.textContent = String(assignedCount);
  els.matchTotalCount.textContent = String(totalCount);
  els.definitionCount.textContent = String(MATCH_DEFINITIONS.length);
  els.answerCount.textContent = String(remainingAnswerCount);
  els.matchCheckButton.disabled = !allAssigned || matchState.checked;

  if (matchState.checked) {
    els.matchStatusText.textContent = `Kontrol edildi: ${matchState.lastScore} / ${totalCount} doğru.`;
  } else if (allAssigned) {
    els.matchStatusText.textContent = "Tüm eşleşmeler hazır.";
  } else if (matchState.selectedDefinitionId !== null) {
    els.matchStatusText.textContent = "Tanım seçili.";
  } else {
    els.matchStatusText.textContent = `${remainingAnswerCount} cevap bekliyor.`;
  }
}

function finishMatchingRound() {
  if (countAssignedAnswers() !== MATCH_PAIRS.length) {
    updateMatchingUi();
    return;
  }

  let correctCount = 0;
  MATCH_DEFINITIONS.forEach((definition) => {
    correctCount += countCorrectAnswers(definition, matchState.assigned.get(definition.id) ?? []);
  });

  matchState.checked = true;
  matchState.lastScore = correctCount;
  renderMatchingResults(correctCount);
  renderMatchingBoard();
}

function renderMatchingResults(correctCount) {
  const totalCount = MATCH_PAIRS.length;
  const wrongCount = totalCount - correctCount;
  const scoreWidth = `${Math.round((correctCount / totalCount) * 100)}%`;

  els.matchResultsPanel.hidden = false;
  els.matchResultsPanel.innerHTML = `
    <h2>Eşleştirme: ${correctCount} / ${totalCount}</h2>
    <div class="result-meter">
      <div class="meter-track"><span class="meter-fill" style="--score-width: ${scoreWidth}"></span></div>
      <div class="result-line">
        <span>${correctCount} doğru</span>
        <span>${wrongCount} yeniden düşün</span>
      </div>
    </div>
  `;
  els.matchResultsPanel.scrollIntoView({ behavior: "smooth", block: "nearest" });
}

function resetMatchingRound() {
  matchState.definitionOrder = shuffle(MATCH_DEFINITIONS.map((definition) => definition.id));
  matchState.answerOrder = shuffle(MATCH_ANSWERS.map((answer) => answer.id));
  matchState.selectedDefinitionId = null;
  matchState.assigned.clear();
  matchState.checked = false;
  matchState.lastScore = null;
  els.matchResultsPanel.hidden = true;
  els.matchResultsPanel.innerHTML = "";
  renderMatchingBoard();
}

function handleKeyboardAssign(event) {
  if (event.target.matches("input, textarea, select")) {
    return;
  }

  const key = event.key.toLocaleUpperCase("tr-TR");
  const category = CATEGORIES.find((item) => item.key === key);
  if (!category) {
    return;
  }

  event.preventDefault();
  assignSelectedToCategory(category.id);
}

