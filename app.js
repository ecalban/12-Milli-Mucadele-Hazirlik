function setCurrentEmpty() {
  els.currentCardSlot.innerHTML = '<p class="empty-state">Kart bekliyor</p>';
}

function drawFact() {
  if (state.currentId !== null || state.remaining.length === 0) {
    return;
  }

  const nextId = state.remaining.pop();
  state.currentId = nextId;
  els.currentCardSlot.innerHTML = "";
  const card = createCard(factById.get(nextId));
  els.currentCardSlot.append(card);
  selectCard(card, { preventScroll: true });
  updateUi();
}

function selectCard(card, options = {}) {
  document.querySelectorAll(".knowledge-card.is-selected").forEach((selectedCard) => {
    selectedCard.classList.remove("is-selected");
    selectedCard.setAttribute("aria-pressed", "false");
  });

  card.classList.add("is-selected");
  card.setAttribute("aria-pressed", "true");
  state.selectedId = Number(card.dataset.id);
  card.focus({ preventScroll: Boolean(options.preventScroll) });
  updateUi();
}

function getSelectedCard() {
  const selectedId = state.selectedId ?? state.currentId;
  if (selectedId === null) {
    return null;
  }
  return document.querySelector(`.knowledge-card[data-id="${selectedId}"]`);
}

function assignSelectedToCategory(categoryId) {
  const card = getSelectedCard();
  const zone = document.querySelector(`.drop-zone[data-category="${categoryId}"]`);
  if (!card || !zone) {
    return;
  }

  moveCard(card, zone);
}

function clearCheckState() {
  if (!state.checked) {
    return;
  }

  state.checked = false;
  state.lastScore = null;
  els.resultsPanel.hidden = true;
  els.resultsPanel.innerHTML = "";
  document.querySelectorAll(".knowledge-card").forEach((card) => {
    removeResultNote(card);
    card.classList.remove("correct", "wrong");
  });
}

function moveCard(card, zone) {
  clearCheckState();

  const id = Number(card.dataset.id);
  const previousZone = card.closest(".drop-zone");
  const wasCurrentCard = state.currentId === id;

  removeResultNote(card);
  card.classList.remove("correct", "wrong", "is-selected");
  card.setAttribute("aria-pressed", "false");
  zone.append(card);
  state.placed.set(id, zone.dataset.category);
  state.selectedId = null;

  if (wasCurrentCard) {
    state.currentId = null;
    setCurrentEmpty();
  }

  if (wasCurrentCard && state.remaining.length > 0) {
    drawFact();
    return;
  }

  if (previousZone !== zone) {
    updateUi();
  }
}

